#include<stdio.h>
#include<unistd.h>

int	ft_str_is_alpha	(char *str)
{
	int		i;
	char	v;

	i = 0;
	v = 1;
	if (*str != 0)
	{
		while (*(str + i) != '\0' && v != 0)
		{
			v = 0;
			if (*(str + i) > 64 || *(str + i) > 96 )
			{
				if (*(str + i) < 91 || *(str + i) < 123)
				{
					v = 1;
					i++;
				}
			}
		}
		return (v);
	}
	v = 1;
	return (v);
}
